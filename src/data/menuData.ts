export function getCategoryBySlug(slug: string) {
  return categories.find((c) => c.slug === slug);
}

export function getItemsForCategory(categoryId: string) {
  return items.filter((item) => item.categoryId === categoryId);
}

/** Derive country code from section name when item.countryCode is not set */
export function getCountryCodeForItem(item: MenuItem): string | undefined {
  if (item.countryCode) return item.countryCode;
  const s = (item.section ?? "").toUpperCase();
  if (s.includes("INDIAN")) return "IN";
  if (s.includes("SUSHI") || s.includes("RAMEN") || s.includes("DIM SUM") || s.includes("CHEUNG FUN") || s.includes("ROBATA")) return "JP";
  if (s.includes("THAI") || s.includes("PAD THAI") || s.includes("SOM TAM") || s.includes("TOM YUM") || s.includes("TOM KHA")) return "TH";
  if (s.includes("KIMCHI") || s.includes("BULGOGI")) return "KR";
  if (s.includes("ARABIC") || s.includes("FATTOUSH") || s.includes("TABBOULEH") || s.includes("HUMMUS")) return "LB";
  if (s.includes("VIETNAMESE")) return "VN";
  if (s.includes("MALAY") || s.includes("PENANG")) return "MY";
  if (s.includes("CANTONESE") || s.includes("PEKING") || s.includes("HAKKA")) return "CN";
  return undefined;
}

/** True when section indicates vegetarian (e.g. "SOUPS - VEGETARIAN"), false for "NON-VEGETARIAN" */
export function isVegetarianSection(section: string | undefined): boolean {
  const s = (section ?? "").toUpperCase();
  if (s.includes("NON-VEGETARIAN")) return false;
  return s.includes("VEGETARIAN");
}

/** ISO 3166-1 alpha-2 to flag emoji (e.g. "JP" -> 🇯🇵) */
export function countryCodeToFlag(code: string): string {
  return [...code.toUpperCase()]
    .map((c) => String.fromCodePoint(0x1f1e6 + c.charCodeAt(0) - 65))
    .join("");
}

